    private void btnAddEstudianteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddEstudianteActionPerformed
        // TODO add your handling code here:
        String nombre = txtNombre.getText();
        long CI = Long.parseLong(txtCI.getText());
        String direccion = txtDireccion.getText();
        long codigo = Long.parseLong(txtCodigo.getText());
        String carrera = txtCarrera.getText();
        
        UAM.agregarEstudiante(nombre, (int)CI, direccion, codigo, 0, carrera);
        JOptionPane.showMessageDialog(this, "Agregado con éxito");
        txtNombre.setText("");
        txtCI.setText("");
        txtDireccion.setText("");
        txtCodigo.setText("");
        txtCarrera.setText("");
    }//GEN-LAST:event_btnAddEstudianteActionPerformed

