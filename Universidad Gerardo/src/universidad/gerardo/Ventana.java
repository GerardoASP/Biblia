    private void btnBuscarTeacherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarTeacherActionPerformed
        // TODO add your handling code here:
        String nameDepartamento = JOptionPane.showInputDialog("Ingrese el nombre del departamento: ");
        Departamento encontradoDos = UAM.buscarDepartamento(nameDepartamento);
        if(encontradoDos != null){
            String nombre = JOptionPane.showInputDialog("Ingrese el nombre del Profesor a buscar");
            Profesor profesorEncontrado = encontradoDos.buscarProfesor(nombre);
            if(profesorEncontrado != null){
                txtResultados.setText(encontradoDos.buscarProfesor(nombre).toString());
            } else{
                JOptionPane.showMessageDialog(this, "No se encontró ningpun Profesor con el nombre buscado");
            }
        }else{
            JOptionPane.showMessageDialog(this, "No se encontró ningpun Departamento con el nombre buscado");
        }
    }//GEN-LAST:event_btnBuscarTeacherActionPerformed

